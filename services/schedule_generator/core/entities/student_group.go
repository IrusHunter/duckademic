// StudentGroup represents a university student group in a scheduling context.
//
// The model disallows simultaneous classes and day overloads.
type StudentGroup struct {
	BusyGrid                         // Availability grid.
	LoadService                      // Manages and validates assigned study loads.
	LessonTypeBinder                 // Handles lesson type binding logic.
	ID               uuid.UUID       // Unique identifier of the StudentGroup.
	Name             string          // Human-readable identifier of the StudentGroup.
	MaxLessonsPerDay int             // Day load limit.
	connectedGroups  []*StudentGroup // Groups that share students with this group.
	StudentNumber    int             // Number of students.
}

// NewStudentGroup creates a new StudentGroup instance.
//
// It requires student group id, name, max lessons per day (dl), number of students (sn),
// busy grid (bg), lesson type binder (ltb) and load service (lc).
func NewStudentGroup(
	id uuid.UUID, name string, dl int, sn int,
	bg *BusyGrid, ltb LessonTypeBinder, lc LoadService,
) *StudentGroup {
	return &StudentGroup{ID: id,
		Name:             name,
		MaxLessonsPerDay: dl,
		BusyGrid:         *bg,
		LessonTypeBinder: ltb,
		LoadService:      lc,
		StudentNumber:    sn,
	}
}

// NewDefaultStudentGroup creates a new StudentGroup instance with default configuration.
//
// It requires student group id, name, max lessons per day (dl),
// number of students (sn), and busy grid (bg).
func NewDefaultStudentGroup(id uuid.UUID, name string, dl int, sn int, bg *BusyGrid) *StudentGroup {
	return NewStudentGroup(id, name, dl, sn, bg, NewLessonTypeBinder(), NewLoadService())
}

